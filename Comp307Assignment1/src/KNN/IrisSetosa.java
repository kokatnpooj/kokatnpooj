package KNN;
public class IrisSetosa {
	String irisType;
	double sepalLength;
	double sepalWidth;
	double petalLength;
	double petalWidth;

	public IrisSetosa(String irisType, double sepalLength, double sepalWidth, double petalLength, double petalWidth) {
		irisType = this.irisType;
		sepalLength = this.sepalLength;
		sepalWidth = this.sepalWidth;
		petalLength = this.petalLength;
		petalWidth = this.petalWidth;

	}

}
